  <h1>About</h1>
  <h2>What is King of Stackers?</h2>
  <p>
    King of Stackers is a turn-based Tetris game. Players take turns placing
    seven pieces until one player tops out. It sounds simple, but it's
    surprisingly fun - and an excellent way to learn both strategy and piece
    manipulation.
  </p>
  <h2>Getting Started</h2>
  <p>
    The most useful page is the Activity section. It shows you the state of your
    active games and any challenges that are available to you. The Public
    Challenges section is where general challenges are shown. You are welcome to
    take these challenges too, but be aware that since they are open for anyone
    to take, some players will keep putting up new ones. Unless you want
    multiple games going with the same person, I suggest you only take a public
    challenge once.
  </p>
  <p>
    Related are the Challenges and the Games pages. The former allows you to
    review your outgoing challenges as well as any incoming ones, and issue new
    challenges. You can also issue challenges from a player's profile, which you
    can get to by clicking their name in the activity list or the member list.
  </p>
